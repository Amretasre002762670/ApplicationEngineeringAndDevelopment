    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        ArrayList<Hospital> searchResultList = new ArrayList<Hospital>();
        validSearch = searchFieldValidation(txtSearch.getText());
        for (int i=0; i<hosList.checkHospitalListSize(); i++) {
            if(validSearch) {
                if(SearchTypeGroup.getSelection().getActionCommand().equals("Community")) {
                    searchResultList=hosList.searchHosCommunity(searchText);
                    populateTable(searchResultList);
                } else if (SearchTypeGroup.getSelection().getActionCommand().equals("HosName")) {
                    searchResultList=hosList.searchHosName(searchText);
                    populateTable(searchResultList);
                } else if (SearchTypeGroup.getSelection().getActionCommand().equals("HosPincode")) {
                    searchResultList=hosList.searchHosPincode(searchValue);
                    populateTable(searchResultList);
                }
            }

        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnUpdateDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateDetailsActionPerformed
        // TODO add your handling code here:
        int hosInd = tblHospitalsView.getSelectedRow();
        
        if(hosInd < 0 ) {
            //if there is no row selected then a dialog is displayed
            JOptionPane.showMessageDialog(this, "Select the Hospital to be Updated.");
            return;
        }
        
        DefaultTableModel hosTable = (DefaultTableModel) tblHospitalsView.getModel();
        
        Hospital selectedHos = (Hospital) hosTable.getValueAt(hosInd, 0);
        
        String nameHos = txtUpdateName.getText();
        String cityHos = txtUpdateCity.getText();
        String commHos = txtUpdateCommunity.getText();
        long pincodeHos = Long.parseLong(txtUpdatePincode.getText());
        long helplineHos = Long.parseLong(txtUpdateHelpline.getText());
        
        Hospital updateHosDetails = hosList.updateHospitalDetails(selectedHos, hosInd);
        
        updateHosDetails.setHosName(nameHos);
        updateHosDetails.setHosCity(cityHos);
        updateHosDetails.setHosCommunity(commHos);
        updateHosDetails.setPicode(pincodeHos);
        updateHosDetails.setHosHelpLine(helplineHos);
        
        JOptionPane.showMessageDialog(this, "Updated Hospital Details");
        
        txtUpdateName.setText("");
        txtUpdateCity.setText("");
        txtUpdateCommunity.setText("");
        txtUpdatePincode.setText("");
        txtUpdateHelpline.setText("");
    }//GEN-LAST:event_btnUpdateDetailsActionPerformed
    
    public boolean searchFieldValidation(String searchTxt) {
        boolean result = false;
        if(SearchTypeGroup.getSelection().getActionCommand().equals("HosPincode")){ 
            try {
                searchValue = Long.parseLong(searchTxt);
                result = true;
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Enter a valid value!");
                txtSearch.setText("");
            }
        } else if(searchTxt.equals("")){
            JOptionPane.showMessageDialog(this, "The search cannot be empty!"); 
            result = false;

        } else {
            searchText = searchTxt;
            result = true;
        }
        return result;
    }
    
    public void populateTable(ArrayList<Hospital> searchReslutsList) {
        DefaultTableModel hosTable = (DefaultTableModel) tblHospitalsView.getModel();
        hosTable.setRowCount(0);
        for(Hospital hos: searchReslutsList) {
            Object[] row = new Object[5];
            row[0] = hos;
            row[1] = hos.getHosCity();
            row[2] = hos.getHosCommunity();
            row[3] = hos.getPicode();
            row[4] = hos.getHosHelpLine();
            
            hosTable.addRow(row);
        }
    }

