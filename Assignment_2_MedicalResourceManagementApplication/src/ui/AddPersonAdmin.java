    private void txtUserNameFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtUserNameFocusLost
        // TODO add your handling code here:
        personName = checkUserName(txtUserName.getText());
        if(!isValidName) {
            lblWarningUserName.setVisible(true);
            txtUserName.setText("");
        } else if (txtUserName.getText().equals("")) {
            lblWarningUserName.setVisible(false);
        }
    }//GEN-LAST:event_txtUserNameFocusLost

    private void btnAddLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddLoginActionPerformed
        // TODO add your handling code here:
        personPasswd = txtPassword.getText();
        if(personName.equals("") || personPasswd.equals("")) {
            
            JOptionPane.showMessageDialog(this, "All Fields should be filled");
            
        } else if(personList.searchPersonByPatientName(personName)) {
            
            Login newUser = loginList.addUsers();
            newUser.setUserName(personName);
            newUser.setPassword(personPasswd);
            newUser.setUserType("User");
            
            JOptionPane.showMessageDialog(this, "User Credentials added");
            
            txtUserName.setText("");
            txtPassword.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Create Patient History First");
            txtUserName.setText("");
            txtPassword.setText("");
        }
    }//GEN-LAST:event_btnAddLoginActionPerformed

