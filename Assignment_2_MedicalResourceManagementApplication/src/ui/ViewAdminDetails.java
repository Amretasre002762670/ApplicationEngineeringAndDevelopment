    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int adminInd = tblViewAdmins.getSelectedRow();
        
        if(adminInd < 0 ) {
            //if there is no row selected then a dialog is displayed
            JOptionPane.showMessageDialog(this, "Select the Hospital to be Updated.");
            return;
        }
        
        DefaultTableModel adminsTable = (DefaultTableModel) tblViewAdmins.getModel();
        
        Login selectedAdmin = (Login) adminsTable.getValueAt(adminInd, 0);
        
        loginList.deleteUser(selectedAdmin);
        
        JOptionPane.showMessageDialog(this, "Deleted the admin");
        
        populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        int adminInd = tblViewAdmins.getSelectedRow();
        
        if(adminInd < 0 ) {
            //if there is no row selected then a dialog is displayed
            JOptionPane.showMessageDialog(this, "Select the Hospital to be Updated.");
            return;
        }
        
        DefaultTableModel adminsTable = (DefaultTableModel) tblViewAdmins.getModel();
        
        Login selectedAdmin = (Login) adminsTable.getValueAt(adminInd, 0);
        
        Login updatedPassword = loginList.updateUserInfo(selectedAdmin, adminInd);
        
        updatedPassword.setPassword(txtUpdatePassword.getText());
        
        JOptionPane.showMessageDialog(this, "Updated Password");
        txtUpdateUserName.setText("");
        txtUpdatePassword.setText("");
        
        populateTable();
    }//GEN-LAST:event_btnSaveActionPerformed

