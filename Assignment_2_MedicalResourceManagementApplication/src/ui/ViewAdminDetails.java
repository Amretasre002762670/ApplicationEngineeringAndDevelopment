    private void btnSearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchButtonActionPerformed
        // TODO add your handling code here:
        String searchText = txtSearchField.getText();
        Login searchResult = new Login();
        ArrayList<Login> serachResultList = new ArrayList<Login>();
        if (txtSearchField.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "This field cannot be empty");
        } else {
            if (btnGrpSearchType.getSelection().getActionCommand().equals("User Name")) {
                searchResult = loginList.searchWithUserName(searchText);
                populateSearchResult(searchResult);
            } else if (btnGrpSearchType.getSelection().getActionCommand().equals("User Type")) {
                serachResultList = loginList.searchWithUserType(searchText);
                populateWithSearchList(serachResultList);
            } else {
                JOptionPane.showMessageDialog(this, "No Search Results Found");
            }
        }
    }//GEN-LAST:event_btnSearchButtonActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int adminInd = tblViewAdmins.getSelectedRow();
        
        if(adminInd < 0 ) {
            //if there is no row selected then a dialog is displayed
            JOptionPane.showMessageDialog(this, "Select the Hospital to be Updated.");
            return;
        }
        
        DefaultTableModel adminsTable = (DefaultTableModel) tblViewAdmins.getModel();
        
        Login selectedAdmin = (Login) adminsTable.getValueAt(adminInd, 0);
        
        txtUpdateUserName.setText(selectedAdmin.getUserName());
        
    }//GEN-LAST:event_btnUpdateActionPerformed

