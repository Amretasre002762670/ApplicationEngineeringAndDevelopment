    public void populateTable() {
        DefaultTableModel adminsTable = (DefaultTableModel) tblViewAdmins.getModel();
        adminsTable.setRowCount(0);
        for (Login login : loginList.getLoginList()) {
            Object[] row = new Object[2];
            row[0] = login;
            row[1] = login.getUserType();

            adminsTable.addRow(row);
        }
    }

    public void populateSearchResult(Login searchResult) {
        DefaultTableModel adminsTable = (DefaultTableModel) tblViewAdmins.getModel();
        adminsTable.setRowCount(0);

        Object[] row = new Object[2];
        row[0] = searchResult;
        row[1] = searchResult.getUserType();

        adminsTable.addRow(row);

    }

    public void populateWithSearchList(ArrayList<Login> searchUsers) {
        DefaultTableModel adminsTable = (DefaultTableModel) tblViewAdmins.getModel();
        adminsTable.setRowCount(0);
        for (Login login : searchUsers) {
            Object[] row = new Object[2];
            row[0] = login;
            row[1] = login.getUserType();

            adminsTable.addRow(row);
        }
    }

