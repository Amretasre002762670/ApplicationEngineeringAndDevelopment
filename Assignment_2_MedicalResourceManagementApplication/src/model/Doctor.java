public class Doctor {

    private long doctorID;
    private String doctorName;
    private String doctorDept;
    private String doctorHospName;
    private String doctorHospComm;
    private long doctorNum;

    public long getDoctorID() {
        return doctorID;
    }

    public void setDoctorID(long doctorID) {
        this.doctorID = doctorID;
    }

    public String getDoctorName() {
        return doctorName;
    }

    public void setDoctorName(String doctorName) {
        this.doctorName = doctorName;
    }

    public String getDoctorDept() {
        return doctorDept;
    }

    public void setDoctorDept(String doctorDept) {
        this.doctorDept = doctorDept;
    }

    public String getDoctorHospName() {
        return doctorHospName;
    }

    public void setDoctorHospName(String doctorHospName) {
        this.doctorHospName = doctorHospName;
    }

    public String getDoctorHospComm() {
        return doctorHospComm;
    }

    public void setDoctorHospComm(String doctorHospComm) {
        this.doctorHospComm = doctorHospComm;
    }

    public long getDoctorNum() {
        return doctorNum;
    }

    public void setDoctorNum(long doctorNum) {
        this.doctorNum = doctorNum;
    }

}
