    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        double temperature = Double.parseDouble(txtTemperature.getText()); // parsing the string field to double
        double bloodPressure = Double.parseDouble(txtPressure.getText());
        int pulse = Integer.parseInt(txtPulse.getText()); // getText extracts the user types in the respective text field
        String date = txtDate.getText();
        
        VitalSigns vs = history.addNewVital(); // this addNewVital method is in VitalSignHistory
        vs.setTemperature(temperature);
        vs.setBloodPressure(bloodPressure);
        vs.setPulse(pulse);
        vs.setDate(date);
        
        JOptionPane.showMessageDialog(this, "New Vital Signs added"); // success message dialog
        
        txtTemperature.setText(""); // clearing the form field after save. Setting to empty strings
        txtPulse.setText("");
        txtPressure.setText("");
        txtDate.setText("");
    }//GEN-LAST:event_btnSaveActionPerformed


